
public class AOps {
 public void add() {
	 int a,b,c;
	 a = 10;
	 b = 20;
	 c = a+b;
	 System.out.println(c);
}
 public void sub() {
	 int d,e,f;
	 d = 20;
	 e = 10;
	 f = d-e;
	 System.out.println(f);
}
 public void mul() {
	 int g,h,i;
	 g = 10;
	 h = 20;
	 i = g*h;
	 System.out.println(i);
 }
 public void div() {
	 int j,k,l;
	 j = 200;
	 k = 100;
	 l = j/k;
	 System.out.println(l);
 }
}
